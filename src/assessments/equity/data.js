export function getScoreColor(score) {
  if (score <= 3) return '#E85D5D';
  if (score <= 6) return '#F5A623';
  return '#4CAF50';
}

export function getScoreLabel(score) {
  if (score <= 3) return 'Opportunity for Growth';
  if (score <= 6) return 'Building Momentum';
  return 'Leading the Way';
}

export function getScoreLevel(score) {
  if (score <= 3) return 'low';
  if (score <= 6) return 'medium';
  return 'high';
}

export function getSectionAverage(sectionId, ratings) {
  const section = sections.find(s => s.id === sectionId);
  if (!section) return 0;
  const sectionRatings = section.criteria
    .map(c => ratings[c.id])
    .filter(r => r !== undefined && r !== null);
  if (sectionRatings.length === 0) return 0;
  return sectionRatings.reduce((a, b) => a + b, 0) / sectionRatings.length;
}

export function getOverallAverage(ratings) {
  const allRatings = Object.values(ratings).filter(r => r !== undefined && r !== null);
  if (allRatings.length === 0) return 0;
  return allRatings.reduce((a, b) => a + b, 0) / allRatings.length;
}

export function calcEquityScores(ratings) {
  const sectionScores = {};
  sections.forEach(s => {
    sectionScores[s.id] = parseFloat(getSectionAverage(s.id, ratings).toFixed(1));
  });
  const overall = parseFloat(getOverallAverage(ratings).toFixed(1));
  return { overall, ...sectionScores };
}
